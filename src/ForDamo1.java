import java.util.Scanner;

public class ForDamo1 {
  public static void main(String[] args) {
    for (int dan = 1; dan <= 9; dan++) {
      System.out.println("--------------");
      for (int i = 1; i <= 9; i++) {
        System.out.printf("%d X %d = %d \n", dan, i, dan*i);
      }
    }
    // 먼저 dan 에 숫자를 넣고 i에 1~9까지 돌리고 그 다음 다시 dan 에 2를 넣고 다시 i에 1~9까지 돌리는 방식
    }
  }
